import { mkdir, readFile, writeFile } from 'node:fs/promises'
import { dirname, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

const root = resolve(dirname(fileURLToPath(import.meta.url)), '..')
const sources = [
  { path: '使用手册.md', label: 'Giverny 使用手册', category: '产品手册' },
  { path: 'docs/AGENT_PRODUCTION_OPERATIONS.md', label: 'Agent 运营手册', category: '运营手册' },
  { path: 'CHANGELOG.md', label: 'Giverny 更新日志', category: '更新日志' },
]

function cleanMarkdown(value) {
  return value
    .replace(/<!--[^]*?-->/g, ' ')
    .replace(/```[^]*?```/g, ' ')
    .replace(/!\[[^\]]*\]\([^)]*\)/g, ' ')
    .replace(/\[([^\]]+)\]\([^)]*\)/g, '$1')
    .replace(/[ \t]+/g, ' ')
    .replace(/\n{3,}/g, '\n\n')
    .trim()
}

function splitLongSection(content, maxLength = 2200) {
  if (content.length <= maxLength) return [content]
  const paragraphs = content.split(/\n\s*\n/)
  const chunks = []
  let current = ''
  for (const paragraph of paragraphs) {
    if (current && current.length + paragraph.length + 2 > maxLength) {
      chunks.push(current.trim())
      current = ''
    }
    if (paragraph.length > maxLength) {
      for (let index = 0; index < paragraph.length; index += maxLength) {
        if (current) chunks.push(current.trim())
        current = paragraph.slice(index, index + maxLength)
      }
    } else {
      current = current ? `${current}\n\n${paragraph}` : paragraph
    }
  }
  if (current.trim()) chunks.push(current.trim())
  return chunks
}

function parseSections(markdown, source) {
  const lines = markdown.split(/\r?\n/)
  const sections = []
  let heading = source.label
  let buffer = []
  const flush = () => {
    const content = cleanMarkdown(buffer.join('\n'))
    if (content.length < 24) return
    splitLongSection(content).forEach((chunk, index) => {
      sections.push({
        id: `${source.path}#${sections.length + 1}`,
        sourcePath: source.path,
        sourceLabel: source.label,
        category: source.category,
        heading: index === 0 ? heading : `${heading}（续）`,
        content: chunk,
      })
    })
  }
  for (const line of lines) {
    const match = line.match(/^#{1,4}\s+(.+)$/)
    if (match) {
      flush()
      heading = cleanMarkdown(match[1]) || source.label
      buffer = []
    } else {
      buffer.push(line)
    }
  }
  flush()
  return sections
}

const documents = []
for (const source of sources) {
  const markdown = await readFile(resolve(root, source.path), 'utf8')
  documents.push(...parseSections(markdown, source))
}

const outputPath = resolve(root, 'src/generated/productKnowledge.generated.ts')
await mkdir(dirname(outputPath), { recursive: true })
await writeFile(outputPath, `// Generated by scripts/generate-product-knowledge.mjs. Do not edit manually.\n\nexport type ProductKnowledgeDocument = {\n  id: string\n  sourcePath: string\n  sourceLabel: string\n  category: string\n  heading: string\n  content: string\n}\n\nexport const productKnowledgeDocuments: ProductKnowledgeDocument[] = ${JSON.stringify(documents, null, 2)}\n`)
process.stdout.write(`Generated ${documents.length} product knowledge sections from ${sources.length} documents.\n`)
